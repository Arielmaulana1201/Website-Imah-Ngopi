            <h3>Imah<span> Ngopi</span></h3>
            <p>Imah Ngopi memiliki tiga relief di dinding teras yang telah ada sejak rumah ini dibangun pada tahun 1950-1960an.
                Relief ini dibuat oleh Abah/Aki Andadinata, tokoh silat Jawa Barat dan pendiri Gerak Badan Pencak Margaluyu Pusat. Relief pertama, Tjinta & Asih, menggambarkan cinta Abah Andadinata kepada istrinya, Ibu Sukaesih, seorang guru SD di Cikuya.
                Setelah Abah Andadinata meninggal pada 1969, Ibu Sukaesih tetap menempati rumah ini hingga wafat di Tanah Suci pada 1982. Relief kedua, Bayawak & Kuya, mengisahkan tempat kelahiran Abah Andadinata di Rancabayawak dan pertemuannya dengan Ibu Sukaesih di Cikuya.
                Rumah ini dimiliki oleh keluarga kami sejak 2007 dan direstorasi pada 2023 menjadi Imah Ngopi, tempat bersantai sambil ngopi yang tetap mempertahankan keindahan sejarahnya.</p>
